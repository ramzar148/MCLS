    <h2>‚úÖ Work Order Approvals</h2>
    
    <h3>Reviewing Work Orders</h3>
    <p>Work orders require manager approval when they involve:</p>
    <ul>
        <li>Significant material costs</li>
        <li>External contractor services</li>
        <li>Major equipment repairs</li>
        <li>Budget impact</li>
    </ul>

    <h3>Approval Process</h3>
    <ol>
        <li>Go to <strong>Work Orders</strong></li>
        <li>Review pending work orders</li>
        <li>Check estimated costs and materials needed</li>
        <li>Verify budget availability</li>
        <li>Approve or reject with comments</li>
    </ol>

    <h3>What to Review</h3>
    <ul>
        <li><strong>Estimated Costs:</strong> Is the budget reasonable?</li>
        <li><strong>Materials:</strong> Are all items necessary?</li>
        <li><strong>Safety Requirements:</strong> Are proper precautions noted?</li>
        <li><strong>Timeline:</strong> Is the schedule acceptable?</li>
    </ul>
