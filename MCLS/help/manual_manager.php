<div class="manual-section">
    <h2>‚ùì Manager FAQs</h2>
    
    <h3>How do I handle escalated issues?</h3>
    <p>For complex problems, create a work order with detailed requirements and assign to your most experienced technician. Involve admin if cross-department coordination is needed.</p>

    <h3>What if a user disagrees with a resolved call?</h3>
    <p>Review the technician's completion notes, reopen the call if needed, and reassign with specific instructions about remaining issues.</p>

    <h3>How do I track department budget?</h3>
    <p>Review work orders with actual costs, export monthly reports, and sum maintenance expenses for budget planning.</p>

    <h3>Can I see other departments' calls?</h3>
    <p>You have read-only access to other departments for coordination purposes, but can only manage your own department's calls.</p>
