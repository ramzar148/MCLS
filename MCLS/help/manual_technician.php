    <h2>‚ùì Technician FAQs</h2>
    
    <h3>Can I reassign a call to another technician?</h3>
    <p>Contact your manager to reassign calls. Managers have the authority to change assignments.</p>

    <h3>What if I need special parts?</h3>
    <p>Document parts needed in the call comments and notify your manager to create a work order with cost approval.</p>

    <h3>How do I handle emergency calls?</h3>
    <p>Critical priority calls require immediate attention. Drop other tasks and respond immediately, then notify your manager.</p>

    <h3>What if a user's description is unclear?</h3>
    <p>Add a comment requesting clarification. The user and managers will be notified of your question.</p>
