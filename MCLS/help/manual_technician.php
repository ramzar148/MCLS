<div class="manual-section" id="working">
    <h2>‚öôÔ∏è Working on Maintenance Calls</h2>
    
    <h3>Starting Work</h3>
    <ol>
        <li>Open the assigned call</li>
        <li>Review the description and location details</li>
        <li>Update status to <strong>"In Progress"</strong></li>
        <li>Add a comment with your initial assessment</li>
    </ol>

    <h3>Updating Progress</h3>
    <p>Keep stakeholders informed by:</p>
    <ul>
        <li>Adding comments about work performed</li>
        <li>Uploading photos of the issue or repair</li>
        <li>Noting any parts or materials needed</li>
        <li>Estimating completion time</li>
    </ul>

    <h3>Requesting Help</h3>
    <p>If you need assistance:</p>
    <ul>
