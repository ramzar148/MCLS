<div class="manual-section" id="tracking">
    <h2>ğŸ‘ï¸ Tracking Your Maintenance Calls</h2>
    
    <h3>Viewing Your Reported Calls</h3>
    <ol>
        <li>Go to <strong>Maintenance Calls</strong> from the sidebar menu</li>
        <li>Your calls are displayed in the table with their current status</li>
        <li>Click on any call to view full details</li>
    </ol>

    <h3>Understanding Call Status</h3>
    <ul>
        <li><strong>Open:</strong> Call has been logged and is waiting for assignment</li>
        <li><strong>Assigned:</strong> A technician has been assigned to your call</li>
        <li><strong>In Progress:</strong> Technician is actively working on the issue</li>
        <li><strong>Resolved:</strong> Issue has been fixed</li>
        <li><strong>Closed:</strong> Call is complete and verified</li>
        <li><strong>Cancelled:</strong> Call was cancelled (you'll see the reason)</li>
    </ul>

    <h3>Using Filters</h3>
    <p>You can filter calls by:</p>
    <ul>
        <li><strong>Status:</strong> Show only open, closed, or specific status</li>
        <li><strong>Priority:</strong> View high-priority issues first</li>
        <li><strong>Date Range:</strong> See calls from specific time periods</li>
    </ul>
</div>

